package Home;

import java.util.*;
import java.util.LinkedHashSet;

public class Linked_Hash_Set {

	public static void main(String[] args) {
		LinkedHashSet<Integer> h1 = new LinkedHashSet<Integer>();
		h1.add(10);
		h1.add(20);
		h1.add(30);
		h1.add(40);
		
		System.out.println(h1);

	}

}
